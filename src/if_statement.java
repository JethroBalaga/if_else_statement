
public class if_statement {
    public static void main(String[]args){
        int time = 19;
        if(time < 10) {
            System.out.println("good morning");
        }
        else if (time > 18){
            System.out.println("good day");
        } 
        else {
            System.out.println("good evening");
        }
    }
}
